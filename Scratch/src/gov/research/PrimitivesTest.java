class PrimitivesTest {
    public static void main(String[] args) {
        int age = 31;
        System.out.println(" The age is: " + age);

        long population = 7_000_000_000L;
        System.out.println(" The population is: " + population);

        double height = 185.3;
        System.out.println(" The height is: " + height);

        boolean isThursday = true;
        System.out.println(" Today is Thursday: " + isThursday);

        char grade = 'B';
        System.out.println(" I got a " + grade + " on the exam ");

        System.out.println(" My favorite word is \"belly\" ");

        int i = 18;
        System.out.println(i);

        int j = i;
        System.out.println(j);

        i++;
        System.out.println(i++);
        // post-increment

        System.out.println(j);

        int n = 3;
        System.out.println(++n);
        // pre-increment


        System.out.println(n);






    }
}